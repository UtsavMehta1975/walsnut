async function addCasioVintageWatches() {
  console.log('üöÄ Starting to add Casio Vintage watches...');
  
  for (const watch of casioVintageWatches) {
    try {
      console.log(`\nüìù Adding ${watch.brand} ${watch.model}...`);
      
      // Add the product
      const productResponse = await fetch('http://localhost:3000/api/products', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': 'Bearer admin@walnut.com', // Admin email for authorization
        },
        body: JSON.stringify({
          brand: watch.brand,
          model: watch.model,
          referenceNumber: watch.referenceNumber,
          price: watch.price,
          previousPrice: watch.previousPrice,
          condition: watch.condition,
          year: watch.year,
          gender: watch.gender,
          description: watch.description,
          stockQuantity: watch.stockQuantity,
          categories: watch.categories,
          specifications: watch.specifications,
          authenticity: watch.authenticity
        }),
      });

      if (productResponse.ok) {
        const createdProduct = await productResponse.json();
        console.log(`‚úÖ Product created: ${createdProduct.brand} ${createdProduct.model} (ID: ${createdProduct.id})`);
        
        // Add images for the product
        console.log(`üñºÔ∏è Adding ${watch.images.length} images...`);
        for (let i = 0; i < watch.images.length; i++) {
          const imageResponse = await fetch(`http://localhost:3000/api/products/${createdProduct.id}/images`, {
            method: 'POST',
            headers: {
              'Content-Type': 'application/json',
              'Authorization': 'Bearer admin@walnut.com',
            },
            body: JSON.stringify({
              imageUrl: watch.images[i],
              altText: `${watch.brand} ${watch.model} - Image ${i + 1}`,
              isPrimary: i === 0, // First image is primary
              sortOrder: i
            }),
          });

          if (imageResponse.ok) {
            const imageResult = await imageResponse.json();
            console.log(`  ‚úÖ Image ${i + 1} added: ${imageResult.imageUrl}`);
          } else {
            const error = await imageResponse.json();
            console.log(`  ‚ùå Failed to add image ${i + 1}: ${error.error || 'Unknown error'}`);
          }
        }
      } else {
        const error = await productResponse.json();
        console.log(`‚ùå Failed to create product: ${error.error || 'Unknown error'}`);
      }
    } catch (error) {
      console.log(`‚ùå Error adding ${watch.brand} ${watch.model}:`, error.message);
    }
  }
  
  console.log('\nüéâ Finished adding Casio Vintage watches!');
}

// Run the script
addCasioVintageWatches().catch(console.error);
